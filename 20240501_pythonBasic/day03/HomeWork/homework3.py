"""
根据工资计算个人社保缴纳费用
    步骤：在终端中录入工资,根据公式计算,显示缴纳费用
    公式：养老保险8% + 医疗保险2% + 3元 + 失业保险0.2% + 公积金12%
"""
# 1.获取数据
salary = float(input("请输入税前薪资："))

# 2.逻辑计算
# salary1 = salary * 0.08 + salary * 0.02 + 3 + salary * 0.002 + salary * 0.12
# 合并同类项
salary1 = salary * (0.08 + 0.02 + 0.002 + 0.12) + 3

# 3.输出
print(f"个人缴纳的社保费用：{salary1}")
