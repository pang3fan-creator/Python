# 1. 字符串 转 整数
number1 = int("20")
print(number1,type(number1))

# 2. 整数 转 字符串
str1 = str(20)
print(str1,type(str1))

# 3. 字符串 转 小数
float1 = float("10")
print(float1,type(float1))

# 4. 小数 转 整数 舍去小数内容取整
number2 = int(10.89)
print(number2,type(number2))

# 5. 小数 转 整数
number3 = int(-10.9)
print(number3,type(number3))
