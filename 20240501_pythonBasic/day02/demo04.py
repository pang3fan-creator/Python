"""
算术运算符
加+ 减- *乘 **幂 /除 //整除  %取余数
"""

print(5 ** 2)  # 25

# 被除数 / 除数  商
print(5 / 2)  # 2.5 小数商
print(0 / 2)  # 0.0
print(10 / 3)  # 3.3333333333333335 16位
# print(5 / 0)  # 除数不能为0

print(5 // 2)  # 2.5  ==> 2  整数商
print(10 // 3)  # 3.33 ==> 3 整数商
print(12 // 3)  # 4 整数商

print(5 % 2)  # 1
print(4 % 2)  # 0
