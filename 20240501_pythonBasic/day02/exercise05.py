"""
在终端中输入一个四位数，计算每一位相加和
例如：1234 1+2+3+4=10
效果：
请输入四位整数：1234
结果是：10
"""
num = int(input("请输入四位整数："))

# 第一种： 先计算各个值 再累加
# 取个位  1234 => 4
u1 = num % 10
# 取十位 1234 => 123 => 3
u2 = num // 10 % 10
# 取百位 1234 => 12  => 2
u3 = num // 100 % 10
# 取千位 1234 => 1
u4 = num // 1000
result = u1 + u2 + u3 + u4

# 第二种：直接累加
result = 1234
result = num % 10
result += num // 10 % 10
result += num // 100 % 10
result += num // 1000

print(f"结果是：{result}")
