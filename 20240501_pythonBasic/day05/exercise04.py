"""
元组不可变的容器 还能修改值？
"""
name = "张无忌"
names = ["周芷若", "赵敏"]
tuple1 = ("张翠山", name, names)

print(tuple1)  # ('张翠山', '张无忌', ['周芷若', '赵敏'])

name = "无忌哥哥"
tuple1[2][0] = "敏儿"
print(tuple1)  # ('张翠山', '张无忌', ['敏儿', '赵敏'])

# 给上方的元组重新赋值
# 如果试图改变一个不可变容器的值，
# python会新建一个新的对象，并把这个对象地址给到变量
tuple1 = ("哈拉少")
# tuple1[0] = ("哈拉不少")# 报错。 因为不可变
print(tuple1)