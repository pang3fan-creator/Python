"""
根据列表中的数字,重复生成*.
    list01 = [1, 2, 3, 4, 5, 4, 3, 2, 1]
    提示:print("*" * 3) # ***
    效果：
        *
        **
        ***
        ****
        *****
        ****
        ***
        **
        *

"""

list01 = [1, 2, 3, 4, 5, 4, 3, 2, 1]

for i in list01:
    print("*" * i)

# 能读能改
for i in range(len(list01)):
    print("*" * list01[i])
